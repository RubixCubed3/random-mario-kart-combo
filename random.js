const button = document.querySelector("#button");
const driverPic = document.querySelector("#driverPic");
const driverName = document.querySelector("#driverName");
const bodyPic = document.querySelector("#bodyPic");
const bodyName = document.querySelector("#bodyName");
const tirePic = document.querySelector("#tirePic");
const tireName = document.querySelector("#tireName");
const gliderPic = document.querySelector("#gliderPic");
const gliderName = document.querySelector("#gliderName");

const driver = pics.Drivers;
const body = pics.Bodies;
const tire = pics.Tires;
const glider = pics.Gliders;

function randomKartPart(data) {
  return Math.floor(Math.random() * data.length);
}

button.onclick = () => {
  let driverIndex = randomKartPart(driver);
  let bodyIndex = randomKartPart(body);
  let tireIndex = randomKartPart(tire);
  let gliderIndex = randomKartPart(glider);

  driverPic.src = driver[driverIndex].pic;
  driverName.textContent = driver[driverIndex].name;

  bodyPic.src = body[bodyIndex].pic;
  bodyName.textContent = body[bodyIndex].name;

  tirePic.src = tire[tireIndex].pic;
  tireName.textContent = tire[tireIndex].name;

  gliderPic.src = glider[gliderIndex].pic;
  gliderName.textContent = glider[gliderIndex].name;
};
